// effects

void Animation_pt2::drawPicture(int x, int y, bool *p, uint8_t rows, uint8_t cols)
{
    width = dmd.width;
    height = dmd.height;
    for (int j = 0; j < cols; j++)
    {
        if (x + j >= 0)
        {
            for (int i = 0; i < rows; i++)
            {
                if (y + i >= 0)
                {
                    if (*(p + i * cols + j) > 0)
                        dmd.setPixel(x + j, y + i, GRAPHICS_ON);
                    else
                        dmd.setPixel(x + j, y + i, GRAPHICS_OFF);
                }
                else if (y + i < height)
                    break;
            }
        }
        else if (x + j < width)
            break;
    }
}

void Animation_pt2::fadingEffect()
{

    for (int j = 0; j < 2; j++)
    {
        for (int i = 255; i > 50; i--)
        {
            dmdbright.setBrightness(i);
            delay(20);
        }
        for (int i = 50; i < 255; i++)
        {
            dmdbright.setBrightness(i);
            delay(20);
        }
    }

    dmdbright.setBrightness(255);
}

void Animation_pt2::cleaningCurtain()
{
    width = dmd.width;
    height = dmd.height;
    for (int j = 0; j <= width / 2; j++)
    {
        for (int i = 0; i < height; i++)
        {
            dmd.setPixel(j, i, GRAPHICS_OFF);
            dmd.setPixel(width - j, i, GRAPHICS_OFF);
        }
        delay(10);
    }
}

